<?php

class Numbers
{
  public function addition($number, $other_number)
  {
    // Remove first letter of the word
    $result = $number + $other_number;
    return $result;
  }

  public function subtraction($number, $other_number)
  {
    // Remove first letter of the word
    $result = $number - $other_number;
    return $result;
  }

  public function multiplication($number, $other_number)
  {
    // Remove first letter of the word
    $result = $number * $other_number;
    return $result;
  }

  public function division($number, $other_number)
  {
    // Remove first letter of the word
    $result = $number / $other_number;
    return $result;
  }
}
